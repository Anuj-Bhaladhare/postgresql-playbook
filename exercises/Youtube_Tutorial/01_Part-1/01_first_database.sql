-- =====================================================
-- Step 1: Switch to PostgreSQL (Linux Terminal)
-- =====================================================

sudo -i -u postgres

-- =====================================================
-- Step 2: Connect to PostgreSQL Server
-- =====================================================

psql -d postgres

-- =====================================================
-- Step 3: List All Databases
-- =====================================================

\l

-- =====================================================
-- Step 4: Create a New Database
-- =====================================================

CREATE DATABASE persondb;

-- =====================================================
-- Step 5: Connect to a Database
-- =====================================================

\c persondb

-- =====================================================
-- Step 6: Show Current Database
-- =====================================================

SELECT current_database();

-- =====================================================
-- Step 7: List All Tables
-- =====================================================

\dt

-- =====================================================
-- Step 8: Create a Table
-- =====================================================

CREATE TABLE person(
    id INT,
    name VARCHAR(100),
    city VARCHAR(100)
);

-- =====================================================
-- Step 9: Describe Table Structure
-- =====================================================

\d person

-- =====================================================
-- Step 10: Insert One Record
-- =====================================================

INSERT INTO person(id, name, city)
VALUES
(1, 'Anuj Bhaladhare', 'Gondia');

-- =====================================================
-- Step 11: Insert Multiple Records
-- =====================================================

INSERT INTO person(id, name, city)
VALUES
(2, 'Sham', 'Mumbai'),
(3, 'Paul', 'Chennai');

-- =====================================================
-- Step 12: View All Records
-- =====================================================

SELECT * FROM person;

-- =====================================================
-- Step 13: Update Existing Record
-- =====================================================

UPDATE person
SET city = 'Mumbai'
WHERE id = 3;

-- =====================================================
-- Step 14: Delete a Record
-- =====================================================

DELETE FROM person
WHERE id = 2;

-- =====================================================
-- Step 15: Create Another Table
-- =====================================================

CREATE TABLE students(
    id INT,
    name VARCHAR(100),
    surname VARCHAR(100),
    class_name VARCHAR(100),
    city VARCHAR(100)
);

-- =====================================================
-- Step 16: Insert Data into Students Table
-- =====================================================

INSERT INTO students
VALUES
(101, 'Rahul', 'Meshram', '12th', 'Gondia'),
(102, 'Shekhar', 'Shaha', '11th', 'Nagpur'),
(103, 'Mohan', 'Kumar', '10th', 'Goa');

-- =====================================================
-- Step 17: View Students Table
-- =====================================================

SELECT * FROM students;

-- =====================================================
-- Step 18: Drop a Table
-- =====================================================

DROP TABLE person;

-- =====================================================
-- Step 19: Drop a Database
-- (Run this after connecting to another database)
-- =====================================================

DROP DATABASE persondb;

-- =====================================================
-- Step 20: Show Current User
-- =====================================================

SELECT current_user;

-- =====================================================
-- Step 21: Exit PostgreSQL
-- =====================================================

\q
